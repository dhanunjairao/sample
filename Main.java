import java.util.*;

public class Main {
    public static void main(String[] args) {
        int n = 1;
        do {
            System.out.println("Say Hello");
        } while (false);
        while (n <= 5) {
            System.out.print(n + " ");
            n++;
        }
        System.out.println();
        System.out.println("Program Exited");
    }
}